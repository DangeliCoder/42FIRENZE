#include <stdlib.h>

char	*ft_strchr(const char *s, int c)
{
	char	*res;

	c %= 256;
	res = (char *)s;
	while (*res != c && *res != '\0')
		res++;
	if (*res != c)
		return (NULL);
	return (res);
}
